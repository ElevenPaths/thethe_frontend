function ioc_scan(text) {
  // Protected IOCs
  text = text
    .replace(/hxxp/gi, "http")
    .replace(/\[.\]/gi, ".")
    .replace(/[\"\'\(]http/gi, "http");
  text = text.split("\n");

  let collected = [];

  text.map((line) => {
    let m = line.match(regexp_url);
    if (m) {
      collected.push(m);
    }
    m = line.match(regexp_ipv4);
    if (m) {
      collected.push(m);
    }
    m = line.match(regexp_hash);
    if (m) {
      collected.push(m);
    }
    m = line.match(regexp_email);
    if (m) {
      collected.push(m);
    }
    m = line.match(regexp_domain);
    if (m && tlds.includes(m[0].split(".").slice(-1)[0])) {
      collected.push(m);
    }
  });

  return collected.flat();
}

